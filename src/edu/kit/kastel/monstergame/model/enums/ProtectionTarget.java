package edu.kit.kastel.monstergame.model.enums;

/**
 * Represents the different types of protection that can be applied to monsters.
 *
 * @author uuifx
 */
public enum ProtectionTarget {
    /**
     * Protects against damage from opponent attacks.
     */
    HEALTH,

    /**
     * Protects against stat reductions from opponent abilities.
     */
    STATS
}